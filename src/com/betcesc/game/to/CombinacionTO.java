package com.betcesc.game.to;

public class CombinacionTO {

	private String idJugada;
	private String combinacion;
	private String idUsuario;
	private String montoApuesta;
	
	public CombinacionTO() {
		
	}

	public String getIdJugada() {
		return idJugada;
	}

	public void setIdJugada(String idJugada) {
		this.idJugada = idJugada;
	}

	public String getCombinacion() {
		return combinacion;
	}

	public void setCombinacion(String combinacion) {
		this.combinacion = combinacion;
	}

	public String getIdUsuario() {
		return idUsuario;
	}

	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}

	public String getMontoApuesta() {
		return montoApuesta;
	}

	public void setMontoApuesta(String montoApuesta) {
		this.montoApuesta = montoApuesta;
	}

}
