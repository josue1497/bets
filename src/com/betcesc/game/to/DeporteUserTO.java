package com.betcesc.game.to;

public class DeporteUserTO {

	private String idDeporte = null;
	private String idUsuario = null;
	private String combinacionUser = null;
	
	public DeporteUserTO() {
		
	}

	public String getIdDeporte() {
		return idDeporte;
	}

	public void setIdDeporte(String idDeporte) {
		this.idDeporte = idDeporte;
	}

	public String getIdUsuario() {
		return idUsuario;
	}

	public void setIdUsuario(String idUsuario) {
		this.idUsuario = idUsuario;
	}

	public String getCombinacionUser() {
		return combinacionUser;
	}

	public void setCombinacionUser(String combinacionUser) {
		this.combinacionUser = combinacionUser;
	}
	
}
